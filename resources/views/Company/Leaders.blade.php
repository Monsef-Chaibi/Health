																		<div class="desc"><p><strong>Dr. Randy Pilgrim</strong> provides executive oversight and direction for SCP Health’s clinical operations, including systems that support clinical excellence, minimize risk, and deliver key results for patients and clinicians.  He also leads SCP’s efforts in health care policy, industry relations, and advocacy, in service of an effective and sustainable system of emergency care in the US.</p>
<p>Dr. Pilgrim completed medical school and residency at the University of Minnesota. He has served in emergency medicine practice and leadership roles for over 30 years and is regularly involved with health policy issues at state and national levels.  His actuarial background, analytical skills, and practical experiences combine with clinical and operational knowledge to balance patient care demands, clinicians’ needs, and key business considerations.</p>
<p>During four terms as Chair of the Board for the Emergency Department Practice Management Association (EDPMA), he represented members that impact over half of the 140 million annual ED visits in the US, working with legislative and regulatory measures that affect quality emergency care.  He now chairs EDPMA’s Federal Health Policy Committee and has co-chaired ACEP’s Task Force on Alternative Payment Models. He serves as a faculty member for ACEP’s ED Director Academy, and has lectured with the IOM&#8217;s dissemination workshop series, <em>&#8220;Hospital-Based Emergency Care, At the Breaking Point”</em>.</p>
<p>As faculty for ACEP’s Scientific Assembly and ED Director Academy, Dr. Pilgrim speaks on a variety of topics related to emergency medicine and health care reform and was twice honored as a Speaker of the Year.</p>
