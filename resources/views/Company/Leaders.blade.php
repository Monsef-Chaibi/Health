																		<div class="desc"><p>Dr. Kip Schumacher is the board-certified Emergency Medicine physician who has used his knowledge of the delivery of Emergency Medicine to develop SCP Health (formerly known as Schumacher Group/Schumacher Clinical Partners). Seventeen years in the trenches of a large hospital emergency department led to the creation of our organization. He knew that with the right team and the right philosophy, we could help shape Emergency Medicine throughout the country. Dr. Schumacher is renowned for his personal commitment to excellence. His focus on high-quality patient care drives our company to respond to our clients and providers with innovative, meaningful solutions, all for the purpose of making healthcare better for every single patient.</p>
<p>Dr. Schumacher is also a national leader in policy arenas, helping shape the future of healthcare. He has published several articles on the leadership challenges facing the healthcare industry, has participated on nonprofit boards dedicated to healthcare reform and has been honored as a “Hero of Emergency Medicine” by the American College of Emergency Physicians (ACEP).</p>
